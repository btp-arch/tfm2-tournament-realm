export function canEditAutomationPolicy(
  tournament: Pick<TournamentRow, "created_by">,
  roles: RoleState,
  userId: string | null,
  isManagedByUser: boolean,
) {
  return Boolean(userId && (roles.isAdmin || tournament.created_by === userId || isManagedByUser));
}

export function getAutomationModeLabel(policyOrMode: TournamentAutomationPolicy | AutomationMode) {
  const mode = typeof policyOrMode === "string" ? policyOrMode : policyOrMode.automationMode;

  return automationModeLabels[mode];
}

export function isAutomationPaused(policy: TournamentAutomationPolicy) {
  return Boolean(policy.automationPausedAt);
}

export function shouldRunAutomaticAutomation(policy: TournamentAutomationPolicy) {
  return policy.automationMode === "automatic" && !isAutomationPaused(policy);
}

export function getAutomationPolicyWarnings(policy: TournamentAutomationPolicy) {
  const warnings: string[] = [];

  if (policy.automationMode === "automatic") {
    warnings.push("Automatic mode can apply enabled safe timer transitions from page activity.");
  }

  if (
    policy.neitherCheckedInBracketPolicy === "random_advancement" ||
    policy.neitherCheckedInGroupPolicy === "random_advancement"
  ) {
    warnings.push("Random advancement is enabled. It advances the tournament but is excluded from public player records.");
  }

  if (policy.automationPausedAt) {
    warnings.push("Automation is paused. Automatic actions will not run until resumed.");
  }

  return warnings;
}

export function getEnabledAutomationToggleLabels(policy: TournamentAutomationPolicy) {
  const labels: string[] = [];

  if (policy.autoCloseRegistrationAtDeadline) labels.push("Close registration at deadline");
  if (policy.autoOpenCheckInAtStartTime) labels.push("Open check-in at start time");
  if (policy.autoCloseCheckInAtDeadline) labels.push("Close check-in at deadline");
  if (policy.autoOpenReplacementWindowIfSpotsAvailable) labels.push("Open replacement window");
  if (policy.autoCloseReplacementWindowAtDeadline) labels.push("Close replacement window");
  if (policy.autoGenerateDrawAfterReplacement) labels.push("Generate draw/bracket after replacement");
  if (policy.autoOpenReadyMatches) labels.push("Open ready matches");
  if (policy.autoApplyMatchTimeoutOutcomes) labels.push("Apply match timeout outcomes");
  if (policy.autoAdvanceGroupRoundWaves) labels.push("Advance group round waves");
  if (policy.autoGeneratePlayoffWhenGroupsResolved) labels.push("Generate playoff when groups resolve");

  return labels;
}

export function isAutomationActionEnabled(
  kind: AutomationActionKind,
  policy: TournamentAutomationPolicy,
) {
  if (kind === "close_registration") return policy.autoCloseRegistrationAtDeadline;
  if (kind === "open_check_in") return policy.autoOpenCheckInAtStartTime;
  if (kind === "apply_check_in_expiry") return policy.autoCloseCheckInAtDeadline;
  if (kind === "apply_replacement_expiry") return policy.autoCloseReplacementWindowAtDeadline;
  if (kind === "generate_group_draw" || kind === "generate_bracket") {
    return policy.autoGenerateDrawAfterReplacement;
  }
  if (kind === "open_ready_matches") {
    return policy.autoOpenReadyMatches || policy.autoAdvanceGroupRoundWaves;
  }
  if (kind === "apply_group_round_expiry" || kind === "apply_bracket_round_expiry") {
    return policy.autoApplyMatchTimeoutOutcomes;
  }
  if (kind === "generate_group_playoff") return policy.autoGeneratePlayoffWhenGroupsResolved;

  return false;
}

export function formatAutomationEvent(event: TournamentAutomationEventRow) {
  const label = event.event_type
    .split("_")
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(" ");

  return `${label} via ${event.source.replace("_", " ")}`;
}

export function generateAutomationRulesText(
  tournament: Pick<
    TournamentRow,
    | "auto_advance_group_round_waves"
    | "auto_apply_match_timeout_outcomes"
    | "auto_apply_timer_outcomes"
    | "auto_close_check_in_at_deadline"
    | "auto_close_registration_at_deadline"
    | "auto_close_replacement_window_at_deadline"
    | "auto_generate_draw_after_replacement"
    | "auto_generate_playoff_when_groups_resolved"
    | "auto_open_check_in_at_start_time"
    | "auto_open_ready_matches"
    | "auto_open_replacement_window_if_spots_available"
    | "automation_mode"
    | "automation_paused_at"
    | "both_checked_in_no_result_policy"
    | "neither_checked_in_bracket_policy"
    | "neither_checked_in_group_policy"
    | "one_checked_in_timeout_policy"
    | "one_report_no_response_policy"
  >,
) {
  const policy = normalizeAutomationPolicy(tournament);
  const neitherPolicyText =
    policy.neitherCheckedInGroupPolicy === policy.neitherCheckedInBracketPolicy
      ? neitherCheckedInTimeoutPolicyLabels[policy.neitherCheckedInGroupPolicy]
      : `Groups: ${neitherCheckedInTimeoutPolicyLabels[policy.neitherCheckedInGroupPolicy]}; bracket: ${neitherCheckedInTimeoutPolicyLabels[policy.neitherCheckedInBracketPolicy]}`;

  return [
    `Automation mode: ${automationModeLabels[policy.automationMode]}. ${
      policy.automationMode === "automatic"
        ? "When a timer expires, enabled safe transitions may be applied automatically from page activity or Live Control polling."
        : "The system shows recommendations, and tournament staff applies actions manually."
    }`,
    policy.autoCloseCheckInAtDeadline
      ? "Check-in may be closed automatically at the configured deadline."
      : "Check-in close requires tournament staff action.",
    policy.autoOpenReplacementWindowIfSpotsAvailable
      ? "If spots are available after check-in closes, the replacement window may open automatically."
      : "Replacement window opening requires tournament staff action.",
    policy.autoCloseReplacementWindowAtDeadline
      ? "The replacement window may close automatically at its deadline."
      : "Replacement close requires tournament staff action.",
    policy.autoGenerateDrawAfterReplacement
      ? "After replacement closes, the draw or bracket may be generated automatically when safe."
      : "Draw and bracket generation require tournament staff action.",
    policy.autoOpenReadyMatches
      ? "Ready matches may open automatically when both players are known."
      : "Ready matches open when tournament staff advances operations.",
    policy.autoApplyMatchTimeoutOutcomes
      ? "Expired group or bracket timers may apply the configured timeout outcomes."
      : "Expired match timers require tournament staff confirmation.",
    `If exactly one player checks into a match room and the opponent does not, policy: ${oneCheckedInTimeoutPolicyLabels[policy.oneCheckedInTimeoutPolicy]}.`,
    `If neither player checks into a match room before timer expiry, policy: ${neitherPolicyText}.`,
    `If both players check in but no result is submitted, policy: ${bothCheckedInNoResultPolicyLabels[policy.bothCheckedInNoResultPolicy]}.`,
    `If one player submits a result and the other does not respond, policy: ${oneReportNoResponsePolicyLabels[policy.oneReportNoResponsePolicy]}.`,
    "Forfeits may affect group standings but do not count toward public player records. BYEs, no-contests, and random timeout advancement do not count toward public player records.",
    "Tournament staff can pause, extend, force close, switch to Manual, or override in emergencies.",
  ].join("\n\n");
}
