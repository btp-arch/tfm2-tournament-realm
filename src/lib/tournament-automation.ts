import type { RoleState } from "@/lib/roles";
import type { TournamentRow } from "@/lib/tournaments";

export type AutomationMode = "manual" | "automatic";
export type OneCheckedInTimeoutPolicy = "forfeit_checked_in" | "staff_review";
export type NeitherCheckedInTimeoutPolicy = "no_contest" | "random_advancement" | "staff_review";
export type BothCheckedInNoResultPolicy = "no_contest" | "staff_review";
export type OneReportNoResponsePolicy = "staff_review";

export type TournamentAutomationPolicy = {
  autoAdvanceGroupRoundWaves: boolean;
  autoApplyMatchTimeoutOutcomes: boolean;
  autoCloseCheckInAtDeadline: boolean;
  autoCloseRegistrationAtDeadline: boolean;
  autoCloseReplacementWindowAtDeadline: boolean;
  autoGenerateDrawAfterReplacement: boolean;
  autoGeneratePlayoffWhenGroupsResolved: boolean;
  autoOpenCheckInAtStartTime: boolean;
  autoOpenReadyMatches: boolean;
  autoOpenReplacementWindowIfSpotsAvailable: boolean;
  automationMode: AutomationMode;
  automationPausedAt: string | null;
  bothCheckedInNoResultPolicy: BothCheckedInNoResultPolicy;
  neitherCheckedInBracketPolicy: NeitherCheckedInTimeoutPolicy;
  neitherCheckedInGroupPolicy: NeitherCheckedInTimeoutPolicy;
  oneCheckedInTimeoutPolicy: OneCheckedInTimeoutPolicy;
  oneReportNoResponsePolicy: OneReportNoResponsePolicy;
};

export type TournamentAutomationEventRow = {
  actor_id: string | null;
  actor_type: "system" | "organizer" | "admin";
  created_at: string;
  details: Record<string, unknown>;
  event_type: string;
  id: string;
  source: "live_control" | "page_poll" | "manual_button";
  tournament_id: string;
};

export type AutomationActionKind =
  | "close_registration"
  | "open_check_in"
  | "apply_check_in_expiry"
  | "apply_replacement_expiry"
  | "generate_group_draw"
  | "generate_bracket"
  | "open_ready_matches"
  | "apply_group_round_expiry"
  | "apply_bracket_round_expiry"
  | "generate_group_playoff";

export const automationModeLabels: Record<AutomationMode, string> = {
  automatic: "Automatic",
  manual: "Manual",
};

export const oneCheckedInTimeoutPolicyLabels: Record<OneCheckedInTimeoutPolicy, string> = {
  forfeit_checked_in: "Checked-in player wins by FF",
  staff_review: "Staff review",
};

export const neitherCheckedInTimeoutPolicyLabels: Record<NeitherCheckedInTimeoutPolicy, string> = {
  no_contest: "No contest",
  random_advancement: "Random advancement",
  staff_review: "Staff review",
};

export const bothCheckedInNoResultPolicyLabels: Record<BothCheckedInNoResultPolicy, string> = {
  no_contest: "No contest",
  staff_review: "Staff review",
};

export const oneReportNoResponsePolicyLabels: Record<OneReportNoResponsePolicy, string> = {
  staff_review: "Staff review",
};

export function getDefaultAutomationPolicy(): TournamentAutomationPolicy {
  return {
    autoAdvanceGroupRoundWaves: true,
    autoApplyMatchTimeoutOutcomes: false,
    autoCloseCheckInAtDeadline: false,
    autoCloseRegistrationAtDeadline: false,
    autoCloseReplacementWindowAtDeadline: false,
    autoGenerateDrawAfterReplacement: false,
    autoGeneratePlayoffWhenGroupsResolved: false,
    autoOpenCheckInAtStartTime: false,
    autoOpenReadyMatches: true,
    autoOpenReplacementWindowIfSpotsAvailable: false,
    automationMode: "manual",
    automationPausedAt: null,
    bothCheckedInNoResultPolicy: "staff_review",
    neitherCheckedInBracketPolicy: "staff_review",
    neitherCheckedInGroupPolicy: "no_contest",
    oneCheckedInTimeoutPolicy: "forfeit_checked_in",
    oneReportNoResponsePolicy: "staff_review",
  };
}

function normalizeMode(value: unknown, fallback: AutomationMode): AutomationMode {
  if (value === "automatic" || value === "hands_off") {
    return "automatic";
  }

  return value === "manual" || value === "assisted" ? "manual" : fallback;
}

function normalizeOneCheckedInPolicy(
  value: unknown,
  fallback: OneCheckedInTimeoutPolicy,
): OneCheckedInTimeoutPolicy {
  return value === "forfeit_checked_in" || value === "staff_review" ? value : fallback;
}

function normalizeNeitherCheckedInPolicy(
  value: unknown,
  fallback: NeitherCheckedInTimeoutPolicy,
): NeitherCheckedInTimeoutPolicy {
  return value === "no_contest" || value === "random_advancement" || value === "staff_review"
    ? value
    : fallback;
}

function normalizeBothCheckedInPolicy(
  value: unknown,
  fallback: BothCheckedInNoResultPolicy,
): BothCheckedInNoResultPolicy {
  return value === "no_contest" || value === "staff_review" ? value : fallback;
}

export function normalizeAutomationPolicy(
  tournament: Partial<TournamentRow> | TournamentAutomationPolicy,
): TournamentAutomationPolicy {
  const defaults = getDefaultAutomationPolicy();

  if ("automationMode" in tournament) {
    return {
      ...defaults,
      ...tournament,
      automationMode: normalizeMode(tournament.automationMode, defaults.automationMode),
      bothCheckedInNoResultPolicy: normalizeBothCheckedInPolicy(
        tournament.bothCheckedInNoResultPolicy,
        defaults.bothCheckedInNoResultPolicy,
      ),
      neitherCheckedInBracketPolicy: normalizeNeitherCheckedInPolicy(
        tournament.neitherCheckedInBracketPolicy,
        defaults.neitherCheckedInBracketPolicy,
      ),
      neitherCheckedInGroupPolicy: normalizeNeitherCheckedInPolicy(
        tournament.neitherCheckedInGroupPolicy,
        defaults.neitherCheckedInGroupPolicy,
      ),
      oneCheckedInTimeoutPolicy: normalizeOneCheckedInPolicy(
        tournament.oneCheckedInTimeoutPolicy,
        defaults.oneCheckedInTimeoutPolicy,
      ),
      oneReportNoResponsePolicy: "staff_review",
    };
  }

  return {
    autoAdvanceGroupRoundWaves:
      tournament.auto_advance_group_round_waves ?? defaults.autoAdvanceGroupRoundWaves,
    autoApplyMatchTimeoutOutcomes:
      tournament.auto_apply_match_timeout_outcomes ??
      tournament.auto_apply_timer_outcomes ??
      defaults.autoApplyMatchTimeoutOutcomes,
    autoCloseCheckInAtDeadline:
      tournament.auto_close_check_in_at_deadline ?? defaults.autoCloseCheckInAtDeadline,
    autoCloseRegistrationAtDeadline:
      tournament.auto_close_registration_at_deadline ?? defaults.autoCloseRegistrationAtDeadline,
    autoCloseReplacementWindowAtDeadline:
      tournament.auto_close_replacement_window_at_deadline ??
      defaults.autoCloseReplacementWindowAtDeadline,
    autoGenerateDrawAfterReplacement:
      tournament.auto_generate_draw_after_replacement ?? defaults.autoGenerateDrawAfterReplacement,
    autoGeneratePlayoffWhenGroupsResolved:
      tournament.auto_generate_playoff_when_groups_resolved ??
      defaults.autoGeneratePlayoffWhenGroupsResolved,
    autoOpenCheckInAtStartTime:
      tournament.auto_open_check_in_at_start_time ?? defaults.autoOpenCheckInAtStartTime,
    autoOpenReadyMatches: tournament.auto_open_ready_matches ?? defaults.autoOpenReadyMatches,
    autoOpenReplacementWindowIfSpotsAvailable:
      tournament.auto_open_replacement_window_if_spots_available ??
      defaults.autoOpenReplacementWindowIfSpotsAvailable,
    automationMode: normalizeMode(tournament.automation_mode, defaults.automationMode),
    automationPausedAt: tournament.automation_paused_at ?? null,
    bothCheckedInNoResultPolicy: normalizeBothCheckedInPolicy(
      tournament.both_checked_in_no_result_policy,
      defaults.bothCheckedInNoResultPolicy,
    ),
    neitherCheckedInBracketPolicy: normalizeNeitherCheckedInPolicy(
      tournament.neither_checked_in_bracket_policy,
      defaults.neitherCheckedInBracketPolicy,
    ),
    neitherCheckedInGroupPolicy: normalizeNeitherCheckedInPolicy(
      tournament.neither_checked_in_group_policy,
      defaults.neitherCheckedInGroupPolicy,
    ),
    oneCheckedInTimeoutPolicy: normalizeOneCheckedInPolicy(
      tournament.one_checked_in_timeout_policy,
      defaults.oneCheckedInTimeoutPolicy,
    ),
    oneReportNoResponsePolicy: "staff_review",
  };
}

